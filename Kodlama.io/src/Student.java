
public class Student extends User{
	
	private double finishLevel;
	private String chooseLesson;
	
	public Student() {
		
	}
	
	public double getFinishLevel() {
		return finishLevel;
	}
	public void setFinishLevel(double finishLevel) {
		this.finishLevel = finishLevel;
	}
	public String getChooseLesson() {
		return chooseLesson;
	}
	public void setChooseLesson(String chooseLesson) {
		this.chooseLesson = chooseLesson;
	}
	

}
